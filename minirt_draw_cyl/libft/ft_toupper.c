#include "libft.h"

char	*ft_toupper(char *bt)
{
	int i;

	i = 0;
	while (bt[i] != '\0')
	{
		if (bt[i] >= 97 && bt[i] <= 122)
			bt[i] = bt[i] - 32;
		i++;
	}
	return (bt);
}
